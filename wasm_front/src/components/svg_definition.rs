#[component]
pub fn ColorIconWithNum(code: String) -> impl IntoView {
    // Parse the code to extract color and number
    let (color_char, number_str) = if code.len() >= 2 {
        let mut chars = code.chars();
        let color = chars.next().unwrap_or('w');
        let number: String = chars.collect();
        (color, number)
    } else {
        ('w', "0".to_string())
    };

    // Get the appropriate color icon component
    let color_icon = match color_char.to_ascii_lowercase() {
        'r' => view! { <IconRed /> }.into_any(),
        'u' => view! { <IconBlue /> }.into_any(),
        'g' => view! { <IconGreen /> }.into_any(),
        'w' => view! { <IconWhite /> }.into_any(),
        'k' => view! { <IconBlack /> }.into_any(),
        'l' => view! { <IconColorless /> }.into_any(),
        _ => view! { <IconWhite /> }.into_any(), // Default to white if unknown
    };

    // Get the number symbol ID
    let number_symbol_id = match number_str.as_str() {
        "0" => "#num0",
        "1" => "#num1",
        "2" => "#num2",
        "3" => "#num3",
        "4" => "#num4",
        "5" => "#num5",
        "6" => "#num6",
        "7" => "#num7",
        "8" => "#num8",
        "9" => "#num9",
        "inf" => "#numinf",
        _ => "#num0", // Default to 0 if unknown
    };

    view! {
        <div style="position: relative; display: inline-block; width: 32px; height: 32px;">
            // Color icon as background
            <div style="position: absolute; top: 0; left: 0; width: 100%; height: 100%;">
                {color_icon}
            </div>
            // Number overlay
            <div style="position: absolute; top: 0; left: 0; width: 100%; height: 100%; pointer-events: none;">
                <svg viewBox="0 0 32 32" width="32" height="32" style="width: 100%; height: 100%;">
                    <use href={number_symbol_id} />
                </svg>
            </div>
        </div>
    }
}

#[component]
pub fn ColorIconsWithNum(code: String) -> impl IntoView {
    // Parse the code to extract multiple color/number pairs
    let parsed_icons = parse_multiple_color_codes(&code);
    
    view! {
        <div style="display: inline-flex; align-items: center; gap: 2px;">
            {parsed_icons.into_iter().map(|icon_code| {
                view! {
                    <ColorIconWithNum code={icon_code} />
                }
            }).collect::<Vec<_>>()}
        </div>
    }
}

// Helper function to parse multiple color/number codes like "w1r3b2"
fn parse_multiple_color_codes(code: &str) -> Vec<String> {
    let mut result = Vec::new();
    let mut current_color = None;
    let mut current_number = String::new();
    
    for ch in code.chars() {
        if "rgwkul".contains(ch.to_ascii_lowercase()) {
            // If we have a previous color/number pair, save it
            if let Some(color) = current_color {
                if current_number.is_empty() {
                    current_number = "0".to_string();
                }
                result.push(format!("{}{}", color, current_number));
            }
            // Start new pair
            current_color = Some(ch.to_ascii_lowercase());
            current_number.clear();
        } else if ch.is_ascii_digit() {
            current_number.push(ch);
        } else if current_number.is_empty() && ch == 'i' {
            // Check for "inf" pattern
            current_number.push(ch);
        } else if current_number == "i" && ch == 'n' {
            current_number.push(ch);
        } else if current_number == "in" && ch == 'f' {
            current_number.push(ch);
        }
    }
    
    // Don't forget the last pair
    if let Some(color) = current_color {
        if current_number.is_empty() {
            current_number = "0".to_string();
        }
        result.push(format!("{}{}", color, current_number));
    }
    
    result
}
